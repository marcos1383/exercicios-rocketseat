// paramêtros (parameters)
const sum = function (number1, number2){
    total = number1 - number2
    return total
}

let number1 = 2022
let number2 = 1998

console.log(`O número 1 é: ${number1}`)
console.log(`O número 2 é: ${number2}`)
console.log(`A soma é ${sum(number1, number2)}`)